    <div class="container">
        <div class="pb-dis-points">
            <div class="pb-dis-point">
                <h5>Overvoltage</h5>
                <p>The need for much higher voltage, or input energy, to drive meaningful amounts of hydrogen
                    production.</p>
            </div>
            <div class="pb-dis-point">
                <h5>Precious Metals</h5>
                <p>Catalysts used for water splitting are often precious
                    metals such as platinum and iridium, a material so rare it can only be found in asteroids,
                    and they all corrode over time.</p>
            </div>
            <div class="pb-dis-point">
                <h5>Membranes</h5>
                <p>Degradable membranes are needed to separate hydrogen (H<sub>2</sub>) and oxygen(O<sub>2</sub>) bubbles so they
                    don’t re-combine to make water (H<sub>2</sub>O).</p>
            </div>
            <div class="pb-dis-point">
                <h5>Distilled Water</h5>
                <p>Precious metals and membranes are highly susceptible to fouling, therefore expensively
                    distilled pure water is required.</p>
            </div>
            <div class="pb-dis-point">
                <h5>2D Reaction Surfaces</h5>
                <p>Water splitting reactions can only happen on the
                    surfaces of 2-dimentional electrode plates. Therefore, much of the water is literally
                    waiting around to be zapped, resulting in low efficiency and low throughput.</p>
            </div>
        </div>
