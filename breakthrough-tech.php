                        <p>For more than 100 years, the gold standard for producing green hydrogen is through
                            electrolysis, using electrolyzers with solar or wind energy to split water into hydrogen and
                            oxygen. However, electrolyzers are very expensive and their
                            efficiencies are fundamentally limited by the natural laws of thermodynamics. For example,
                            the theoretical voltage required to split water is 1.23V, but in real life, the voltage
                            required in an industrial electrolyzer is closer to 2V, sometimes more. This
                            60% or more of additional energy is wasted and not put into hydrogen molecules.</p>
