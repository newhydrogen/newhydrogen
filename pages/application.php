        <p class="text-base sm:text-lg font-medium text-black ">
            Hydrogen is used in cars and trucks of the future as a fuel source for fuel cell vehicles. A hydrogen fuel-cell vehicle uses the same kind of electric motor to turn the wheels that a battery-electric car does. But it’s powered not by a large, heavy battery but by a fuel-cell stack in which pure hydrogen (H2) passes through a membrane to combine with oxygen (O2) from the air, producing the electricity that turns the wheels plus water vapor. Hydrogen trucks are gradually becoming a thing, and in a decade or so they are likely to seriously start replacing the diesel fleets.
        </p>
